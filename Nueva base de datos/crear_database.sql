-- PAR 4: client - comanda  (LEFT OUTER JOIN)
-- ciutat és nova taula de referència (inclou ~100 ciutat)
-- comanda té id_ciutat_enviament (FK a ciutat)
-- ============================================================
DROP TABLE IF EXISTS comanda;
DROP TABLE IF EXISTS client;
DROP TABLE IF EXISTS ciutat;

CREATE TABLE ciutat (
  id INT AUTO_INCREMENT PRIMARY KEY,
  nom VARCHAR(80),
  codi_postal VARCHAR(5)
);

CREATE TABLE client (
  id INT AUTO_INCREMENT PRIMARY KEY,
  nom VARCHAR(50),
  cognom VARCHAR(50),
  email VARCHAR(80),
  telefon VARCHAR(15),
  id_ciutat INT,
  FOREIGN KEY (id_ciutat) REFERENCES ciutat(id)
);

CREATE TABLE comanda (
  id INT AUTO_INCREMENT PRIMARY KEY,
  data_comanda DATE,
  total DECIMAL(10,2),
  estat VARCHAR(20),
  adreca_enviament VARCHAR(150),
  id_client INT NOT NULL,
  id_ciutat_enviament INT,
  FOREIGN KEY (id_client) REFERENCES client(id),
  FOREIGN KEY (id_ciutat_enviament) REFERENCES ciutat(id)
);

